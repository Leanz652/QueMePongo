package prenda;

public class Color {
  int red;
  int green;
  int blue;

  public int getRed(){return this.red;}
  public int getGreen() {return this.green;}
  public int getBlue() {return this.blue;}

  public Color(int red,int green, int blue) {
    this.red = red;
    this.green = green;
    this.blue = blue;
  }
}

